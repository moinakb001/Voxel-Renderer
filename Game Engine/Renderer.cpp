void Renderer::createDescriptorSet() {
	VkDescriptorSetLayout layouts[] = { descriptorSetLayout };
	VkDescriptorSetAllocateInfo allocInfo = {};
	allocInfo.sType = VK_STRUCTURE_TYPE_DESCRIPTOR_SET_ALLOCATE_INFO;
	allocInfo.descriptorPool = descriptorPool;
	allocInfo.descriptorSetCount = 1;
	allocInfo.pSetLayouts = layouts;
	ErrorCheck(vkAllocateDescriptorSets(device, &allocInfo, &descriptorSet), "NO DESCRIPTOR SET FAM");

	VkDescriptorBufferInfo bufferInfo = {};
	bufferInfo.buffer = uniform;
	bufferInfo.offset = 0;
	bufferInfo.range = sizeof(UniformBufferObject);

	VkWriteDescriptorSet descriptorWrite = {};
	descriptorWrite.sType = VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET;
	descriptorWrite.dstSet = descriptorSet;
	descriptorWrite.dstBinding = 0;
	descriptorWrite.dstArrayElement = 0;
	descriptorWrite.descriptorType = VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER;
	descriptorWrite.descriptorCount = 1;
	descriptorWrite.pBufferInfo = &bufferInfo;
	descriptorWrite.pImageInfo = nullptr; // Optional
	descriptorWrite.pTexelBufferView = nullptr; // Optional

	vkUpdateDescriptorSets(device, 1, &descriptorWrite, 0, nullptr);
}
uint32_t Renderer::findMemoryType(uint32_t typeFilter, VkMemoryPropertyFlags properties) {
	VkPhysicalDeviceMemoryProperties memProperties;
	vkGetPhysicalDeviceMemoryProperties(physicalDevice, &memProperties);

	for (uint32_t i = 0; i < memProperties.memoryTypeCount; i++) {
		if ((typeFilter & (1 << i)) && (memProperties.memoryTypes[i].propertyFlags & properties) == properties) {
			return i;
		}
	}

}
void Renderer::createBuffer(VkDeviceSize size, VkBufferUsageFlags usage, VkMemoryPropertyFlags properties, VkBuffer& buffer, VkDeviceMemory& bufferMemory) {
	VkBufferCreateInfo bufferInfo = {};
	bufferInfo.sType = VK_STRUCTURE_TYPE_BUFFER_CREATE_INFO;
	bufferInfo.size = size;
	bufferInfo.usage = usage;
	bufferInfo.sharingMode = VK_SHARING_MODE_EXCLUSIVE;

	if (vkCreateBuffer(device, &bufferInfo, nullptr, &buffer) != VK_SUCCESS) {
		throw std::runtime_error("failed to create buffer!");
	}

	VkMemoryRequirements memRequirements;
	vkGetBufferMemoryRequirements(device, buffer, &memRequirements);

	VkMemoryAllocateInfo allocInfo = {};
	allocInfo.sType = VK_STRUCTURE_TYPE_MEMORY_ALLOCATE_INFO;
	allocInfo.allocationSize = memRequirements.size;
	allocInfo.memoryTypeIndex = findMemoryType(memRequirements.memoryTypeBits, properties);

	if (vkAllocateMemory(device, &allocInfo, nullptr, &bufferMemory) != VK_SUCCESS) {
		throw std::runtime_error("failed to allocate buffer memory!");
	}

	vkBindBufferMemory(device, buffer, bufferMemory, 0);
}
void Renderer::createVertexBuffers() {
	createBuffer(sizeof(VertexInstanced) * voxels.world.size(), VK_BUFFER_USAGE_VERTEX_BUFFER_BIT, VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT, instBuf, instMem);
	
	createBuffer(sizeof(VertexSingle) * cube.size(), VK_BUFFER_USAGE_VERTEX_BUFFER_BIT, VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT, vertex, vertexMem);

	createBuffer(sizeof(uint16_t) * cubeIndex.size(), VK_BUFFER_USAGE_INDEX_BUFFER_BIT, VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT, index, indexMem);

	createBuffer(sizeof(UniformBufferObject), VK_BUFFER_USAGE_UNIFORM_BUFFER_BIT, VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT, uniform, uniformMem);

	void* data;
	vkMapMemory(device, vertexMem, 0, sizeof(VertexSingle) * cube.size(), 0, &data);
	memcpy(data, cube.data(), sizeof(VertexSingle) * cube.size());
	vkUnmapMemory(device, vertexMem);


	vkMapMemory(device, instMem, 0, sizeof(VertexInstanced) * voxels.world.size(), 0, &data);
	memcpy(data, voxels.world.data(), sizeof(VertexInstanced) * voxels.world.size());
	vkUnmapMemory(device, instMem);

	vkMapMemory(device, indexMem, 0, sizeof(uint16_t) * cubeIndex.size(), 0, &data);
	memcpy(data, cubeIndex.data(), sizeof(uint16_t) * cubeIndex.size());
	vkUnmapMemory(device, indexMem);

}

void Renderer::updateUniformBuffer() {
	int width, height;
	glfwGetWindowSize(win, &width, &height);
	UniformBufferObject ubo = {};
	ubo.dist = -distOcc;


	ubo.model = glm::mat4(1.0);

	ubo.view = glm::lookAt(pos, pos + dir, up);

	ubo.proj =perspective? glm::perspective(glm::radians(120.0f), swapChainExtent.width / (float)swapChainExtent.height, 0.1f, 100000.0f): glm::ortho(0.f,1.0f, 0.0f,1.0f, 0.1f, 100000.0f);
	
	ubo.proj[1][1] *= perspective?-1:-1;
	void * data;
	vkMapMemory(device, uniformMem, 0, sizeof(ubo), 0, &data);
	memcpy(data, &ubo, sizeof(ubo));
	vkUnmapMemory(device, uniformMem);
}
